    private void botaoDarBaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDarBaixaActionPerformed
        int id = Integer.parseInt(campoId.getText());
        int quantidade = Integer.parseInt(campoQuantidade.getText());
        boolean checkId = false;
        
        for(ProdutoEstoque x: Principal.produtosEstoque){
            if(id==x.getId()){
                checkId = true;
                Conexao.darbaixa(id,quantidade,x.getQuantidade());
                
                Conexao.iniciarVariaveisProdutoEstoque();
                
                botaoLimpar.doClick();
                botaoListar.doClick();
            }    
        }
        if(checkId == false){
            JOptionPane.showMessageDialog(null, "Código Inexistente!");
            botaoLimpar.doClick();
        }
    }//GEN-LAST:event_botaoDarBaixaActionPerformed

