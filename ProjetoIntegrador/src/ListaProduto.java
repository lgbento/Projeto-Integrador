    private void botaoListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoListarActionPerformed
        
        DefaultTableModel val = (DefaultTableModel) tabelaListar.getModel();
        val.setNumRows(0);
        for(ProdutoEstoque x:Principal.produtosEstoque){
            
        val.addRow(new String[]{String.valueOf(x.getId()),x.getNomeProduto(),x.getMarcaProduto(),
            String.valueOf(x.getQuantidade()),String.valueOf(x.getQuantMinima()),
            String.valueOf(x.getPrecoMedia()),String.valueOf(x.getValorTotal())});
        
    }
    ProdutoEstoque ProdutoEstoqueBaixo = ControlaEstoque.checaEstoque();
    if(ProdutoEstoqueBaixo.getId()!=0){
        int opcao = JOptionPane.showConfirmDialog(null, "Produto "+ProdutoEstoqueBaixo.getNomeProduto()+" | Marca "+ProdutoEstoqueBaixo.getMarcaProduto()+" no estoque mínimo, deseja fazer pedido?");
        if(opcao==0){
            
            for(Fornecedor.ProdutoFornecedor x: Principal.produtosFornecedor){
                if(ProdutoEstoqueBaixo.getNomeProduto().equals(x.getNomeProduto()) && ProdutoEstoqueBaixo.getMarcaProduto().equals(x.getMarcaProduto())){
                    Principal.pedidoImediato = x;
                    dispose(); new FazPedido().setVisible(true);
                }
            }
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Ok! Avisarei Posteriormente!");
        }
    }
    
    
    
        
    }//GEN-LAST:event_botaoListarActionPerformed

