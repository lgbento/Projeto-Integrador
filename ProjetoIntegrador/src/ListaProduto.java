    private void botaoEditarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarProdutoActionPerformed
        int id = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe o código do produto que deseja editar."));
        boolean checkId = false;
        
        for(ProdutoEstoque x:Principal.produtosEstoque){
            if(x.getId()==id){
                checkId = true;EditaProdutoEstoque.temp = x;this.dispose(); new EditaProdutoEstoque().setVisible(true);
            }
        }
        if(checkId == false){
            JOptionPane.showMessageDialog(null, "Código Inexistente;");
        }
        
    }//GEN-LAST:event_botaoEditarProdutoActionPerformed

