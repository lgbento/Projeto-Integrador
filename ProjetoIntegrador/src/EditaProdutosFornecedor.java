    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed

        int id = tempProdutoFornecedor.getId();
        String nomeProduto = campoNomeProduto.getText();
        String marcaProduto = campoMarcaProduto.getText();
        double preco = Double.parseDouble(campoPrecoProduto.getText());
        
        System.out.println(id+" "+nomeProduto+" "+marcaProduto+" "+preco);
        
        Conexao.editaProdutoFornecedor(id,nomeProduto,marcaProduto,preco);
        
        Conexao.iniciarVariaveisProdutoFornecedor();
        
        this.dispose(); new ListaProdutosFornecedores().setVisible(true);
        
    }//GEN-LAST:event_botaoEditarActionPerformed

