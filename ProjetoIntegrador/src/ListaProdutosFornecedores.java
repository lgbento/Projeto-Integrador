    private void botaoListarProdutosFornecedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoListarProdutosFornecedoresActionPerformed
        Conexao.iniciarVariaveisProdutoFornecedor();
        DefaultTableModel val = (DefaultTableModel) tabela.getModel();
        val.setNumRows(0);
        for(Fornecedor.ProdutoFornecedor x:Principal.produtosFornecedor){
            if(tempFornecedor.getId() == x.getIdFornecedor()){
                val.addRow(new String[]{String.valueOf(x.getId()),x.getNomeProduto(),x.getMarcaProduto(),
                String.valueOf(x.getPrecoProduto())});
            }
        }
    }//GEN-LAST:event_botaoListarProdutosFornecedoresActionPerformed

