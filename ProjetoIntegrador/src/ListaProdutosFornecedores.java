    private void botaoEditarProdutoFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarProdutoFornecedorActionPerformed
        boolean checkId = false;
        int id = Integer.parseInt(campoCodigoProduto.getText());
        for(Fornecedor.ProdutoFornecedor x:Principal.produtosFornecedor){
            if(x.getId()==id){
                EditaProdutosFornecedor.tempProdutoFornecedor = x;this.dispose(); new EditaProdutosFornecedor().setVisible(true);checkId = true;
            }
        }
            if(checkId==false){
                JOptionPane.showMessageDialog(null, "Código Inexistente;");
            
            }
        
        
    }//GEN-LAST:event_botaoEditarProdutoFornecedorActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        int id = Integer.parseInt(campoCodigoProduto.getText());
        
        boolean checkId = false;
        
        for(Fornecedor.ProdutoFornecedor x:Principal.produtosFornecedor){
            if(x.getId()==id){
                Conexao.excuirProdutoFornecedor(id);
                
                checkId = true;
                Conexao.iniciarVariaveisProdutoFornecedor();
                }
            }
        if(checkId == false){
                JOptionPane.showMessageDialog(null, "Código Inexistente;");
        } 
    }//GEN-LAST:event_botaoExcluirActionPerformed

