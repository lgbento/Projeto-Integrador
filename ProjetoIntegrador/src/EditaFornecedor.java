        String email = campoEmail.getText();
        String telefone = campoTelefone.getText();

        Conexao.editaFornecedor(idFornecedor,nomeFornecedor,email,telefone);

        Conexao.iniciarVariaveisFornecedor();
        
        botaoLimpar.doClick();
