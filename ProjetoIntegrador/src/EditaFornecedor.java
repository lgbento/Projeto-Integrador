    private void botaoListarFornecedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoListarFornecedoresActionPerformed
        DefaultTableModel val = (DefaultTableModel) tabelaListarFornecedores.getModel();
        val.setNumRows(0);
        for(Fornecedor x:Principal.fornecedores){

            val.addRow(new String[]{String.valueOf(x.getId()),x.getNomeFornecedor(),x.getEmail(),
                x.getTelefone()});

        }
    }//GEN-LAST:event_botaoListarFornecedoresActionPerformed

    private void botaoPreencherCamposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPreencherCamposActionPerformed
        int idFornecedor = Integer.parseInt(campoCodigoFornecedor.getText());
        
        boolean checkId = false;
        
        for(Fornecedor x: Principal.fornecedores){
            if(idFornecedor==x.getId()){
                campoNomeFornecedor.setText(x.getNomeFornecedor());
                campoEmail.setText(x.getEmail());
                campoTelefone.setText(x.getTelefone());
                checkId = true;
            }
        }
        if(checkId==false){
                JOptionPane.showMessageDialog(null, "Código Inválido!");
            }
    }//GEN-LAST:event_botaoPreencherCamposActionPerformed

