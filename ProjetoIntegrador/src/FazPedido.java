    private void botaoPreencherCamposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPreencherCamposActionPerformed
        int idProdutoFornecedor = Integer.parseInt(campoIdProdutoFornecedor.getText());
        
        boolean checkProdutoCadastradoEstoque = false;
        
        for(Fornecedor.ProdutoFornecedor x: Principal.produtosFornecedor){
            if(idProdutoFornecedor==x.getId()){
                Principal.produtoFornecedor = x;
            }
        }

        for(ProdutoEstoque x: Principal.produtosEstoque){
            if(Principal.produtoFornecedor.getNomeProduto().equals(x.getNomeProduto()) && Principal.produtoFornecedor.getMarcaProduto().equals(x.getMarcaProduto())){
                Principal.produto = x;
                campoNomeProduto.setText(x.getNomeProduto());
                campoMarcaProduto.setText(x.getMarcaProduto());
                campoPrecoMedia.setText(String.valueOf(x.getPrecoMedia()));
                campoQuantidadeMinima.setText("já cadastrado");
                campoQuantidadeMinima.setEditable(false);
                checkProdutoCadastradoEstoque = true;
            }   
        }
        if(checkProdutoCadastradoEstoque==false){
                campoNomeProduto.setText(Principal.produtoFornecedor.getNomeProduto());
                campoMarcaProduto.setText(Principal.produtoFornecedor.getMarcaProduto());
                campoPrecoMedia.setText(String.valueOf(Principal.produtoFornecedor.getPrecoProduto()));
            }

    }//GEN-LAST:event_botaoPreencherCamposActionPerformed

    private void botaoListarProdutosFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoListarProdutosFornecedorActionPerformed
        int idFornecedor = Integer.parseInt(campoIdFornecedor.getText());
        for(Fornecedor x: Principal.fornecedores){
            if(idFornecedor==x.getId()){
                campoNomeFornecedor.setText(x.getNomeFornecedor());
            }

        }

        Conexao.iniciarVariaveisProdutoFornecedor();
        boolean checkId = false;
        DefaultTableModel val = (DefaultTableModel) tabelaProdutoFornecedor.getModel();
        val.setNumRows(0);
        for(Fornecedor.ProdutoFornecedor x:Principal.produtosFornecedor){
            if(idFornecedor == x.getIdFornecedor()){
                val.addRow(new String[]{String.valueOf(x.getId()),x.getNomeProduto(),x.getMarcaProduto(),
                    String.valueOf(x.getPrecoProduto())});checkId = true;
        }
        }
        if(checkId==false){
            JOptionPane.showMessageDialog(null, "Código Inexistente;");
        }
    }//GEN-LAST:event_botaoListarProdutosFornecedorActionPerformed

