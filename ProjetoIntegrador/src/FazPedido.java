    private void botaoFazerPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFazerPedidoActionPerformed
        if(campoQuantidadeMinima.getText().equals("já cadastrado")){
           int idPrCliente = Principal.produto.getId();
            int idFornecedor = Integer.parseInt(campoIdFornecedor.getText());
            Date date  = new Date();    
            Calendar c = Calendar.getInstance();
            c.setTime(date);
            int dia = c.get(Calendar.DATE);
            int mes = c.get(Calendar.MONTH);
            int ano = c.get(Calendar.YEAR);
            String dataCompleta = String.valueOf(ano)+"-"+String.valueOf(mes)+"-"+String.valueOf(dia);
            int quantComprada = Integer.parseInt(campoQuantidade.getText());
            int quantidadeEstoque = Principal.produto.getQuantidade();
            int quantidadeTotal = quantidadeEstoque + quantComprada;
            double preco = Double.parseDouble(campoPrecoMedia.getText());
            double valorCompra = preco * quantComprada;
            double valorTotal = quantidadeTotal * preco; 
            
            Conexao.cadastraPedido(idPrCliente,idFornecedor,dataCompleta,preco,quantComprada,valorCompra);
            
            Conexao.adicionaEstoque(idPrCliente,quantidadeTotal,preco,valorTotal);
            Conexao.iniciarVariaveisProdutoEstoque();
            
            Principal.pedidoImediato = new Fornecedor(0,"","","").new ProdutoFornecedor(0,0, "", "", 0);
            botaoLimpar.doClick();
            
            campoQuantidadeMinima.setEditable(true);
            
            
        }else{
            int idFornecedor = Integer.parseInt(campoIdFornecedor.getText());
            String nomeProduto = campoNomeProduto.getText();
            String marcaProduto = campoMarcaProduto.getText();
            int quantidade = Integer.parseInt(campoQuantidade.getText());
            int quantMinima = Integer.parseInt(campoQuantidadeMinima.getText());
            double precoMedia = Double.parseDouble(campoPrecoMedia.getText());
            double valorTotal = quantidade * precoMedia;
            
            int id = Conexao.cadastraProdutoEstoque(nomeProduto,marcaProduto,
            quantidade,quantMinima,precoMedia,valorTotal);
            
            Conexao.iniciarVariaveisProdutoEstoque();
            
            Date date  = new Date();    
            Calendar c = Calendar.getInstance();
            c.setTime(date);
            int dia = c.get(Calendar.DATE);
            int mes = c.get(Calendar.MONTH);
            int ano = c.get(Calendar.YEAR);
            String dataCompleta = String.valueOf(ano)+"-"+String.valueOf(mes)+"-"+String.valueOf(dia);
            
            Conexao.cadastraPedido(id,idFornecedor,dataCompleta,precoMedia,quantidade,valorTotal);
            
            botaoLimpar.doClick();
            campoQuantidadeMinima.setEditable(true);
        }
        
 

       

    }//GEN-LAST:event_botaoFazerPedidoActionPerformed

